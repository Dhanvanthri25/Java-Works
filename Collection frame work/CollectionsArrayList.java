import java.util.*;
class CollectionsArrayList
{
public static void main(String args[])
{
ArrayList<String> arrList = new ArrayList<>();   //main syntax for collectionlists
arrList.add("Cricket");
arrList.add("Hockey");
arrList.add(0,"Basketball");

System.out.println("ArrayList Elements: ");

for(String str:arrList)
System.out.println(str);
}
}





                                                                                                                  